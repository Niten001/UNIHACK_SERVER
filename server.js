function recieveInput(ID, inputType, input) {
    for (const player of players.values()) {
        if (player.id == ID) {
            switch (inputType) {
                case "left":
                    if (input) {
            						console.log("left");
                        player.x -= 1;
                        player.update();
                    } else {
                        player.moveLeft = false;
                    }
                    break;
                case "right":
                    if (input) {
            						console.log("right");
                        player.x += 1;
                        player.update();

                    } else {
                        player.moveRight = false;
                    }
                    break;
                case "space":
                    if (input) {
            						console.log("space");
                        player.jump = true;
                        player.update();
                    } else {
                        player.jump = false;
                    }
                    break;
                default:
                    break;
            }
        }
	}
}

